package com.sg.D1ABeginning;
public class BestAdderEver {
    public static void main(String[] args) {
        int numFirst = 3;
        int numSecond = -60;
        double numThird = 12.5;
        double sum;
        
        System.out.println("First number is: "+numFirst);
        System.out.println("Second number is: "+numSecond);
        System.out.println("Third number is: "+numThird);
        
        sum = numFirst + numSecond + numThird;
        
        System.out.println("The sum is: "+sum);
        System.out.println("And when you think about it, it's still "+sum);
    }
}
