package com.sg.PP7Fors;

public class ForAndTwentyBlackbirds {
    public static void main(String[] args) {
        
        int i;
        
        for (i=0;i<24;i++){
            System.out.println("Blackbird #"+(i+1)+" in the pie.");
        }
        System.out.println("Now there are "+i+" birds in the pie.");
    }
}